#!/usr/bin/env node
// Vendor the Sigma-Profile "jorvis" charter render into a generated TS module
// (P2 design decision D2). SigmaLink ships Jorvis's operator persona as an
// inlined string constant — no runtime file resolution, no packaging
// changes: esbuild inlines charter-default.ts's export straight into the
// bundle. Re-run this after any Sigma-Profile jorvis-target re-render
// (`node build/render.mjs` in Sigma-Profile) to pick up charter changes.
//
// Usage: node scripts/sync-jorvis-charter.cjs [source-path]
// With no args, reads the sibling Sigma-Profile checkout's rendered output
// at DEFAULT_SOURCE below.

const fs = require('node:fs');
const path = require('node:path');

const DEFAULT_SOURCE = '/Users/aisigma/projects/Sigma-Profile/dist/jorvis/system-prompt.md';
const OUT_FILE = path.join(__dirname, '..', 'src', 'main', 'core', 'operator', 'charter-default.ts');

const sourcePath = process.argv[2] || DEFAULT_SOURCE;

if (!fs.existsSync(sourcePath)) {
  console.error(`[sync-jorvis-charter] source not found: ${sourcePath}`);
  console.error(
    '[sync-jorvis-charter] render it first: cd /Users/aisigma/projects/Sigma-Profile && node build/render.mjs',
  );
  process.exit(1);
}

const content = fs.readFileSync(sourcePath, 'utf8');

if (!content.trim()) {
  console.error(`[sync-jorvis-charter] source is empty: ${sourcePath}`);
  process.exit(1);
}

const out =
  '// GENERATED by scripts/sync-jorvis-charter.cjs — DO NOT hand-edit; re-run the script.\n' +
  `export const JORVIS_CHARTER_DEFAULT: string = ${JSON.stringify(content)};\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, out, 'utf8');
console.log(`[sync-jorvis-charter] wrote ${OUT_FILE} (${content.length} chars)`);
